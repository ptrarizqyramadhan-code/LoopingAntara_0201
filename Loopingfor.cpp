#include <iostream>
using namespace std;

int main() {
    //perulangan dengan FOR
    cout << "Perulangan  FOR" << endl;

    //perulangan dengan FOR untuk naik
    for (int i = 1; i <= 5; i++) {
        cout << "Nilai i : " << i << " Teknologi Informasi" << endl;
    }
    cout << endl;

// perulangan dengan  FOR untuk turun 
    cout << "Perulangan  FOR untuk turun" << endl;
    for (int i = 5; i >= 1; i--) {
        cout << "Nilai i : " << i << " Teknologi Informasi UMY " << endl;
    }
    cout << endl;

    return 0;
}